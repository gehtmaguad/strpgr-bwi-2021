package Class1.Calculations;

public class IntegerArithmetic {
    public static void main(String[] args) {
        System.out.printf("%d + %d = %d\n", 19, 4, 19 + 4);
        System.out.printf("%d - %d = %d\n", 19, 4, 19 - 4);
        System.out.printf("%d * %d = %d\n", 19, 4, 19 * 4);
        System.out.printf("%d / %d = %d\n", 19, 4, 19 / 4);
        System.out.printf("%d %% %d = %d\n", 19, 4, 19 % 4);
        // System.out.printf("%d / %d = %d\n", 19, 0, 19 / 0);

        System.out.printf("%d\n", Integer.MAX_VALUE);
        System.out.printf("%d\n", Integer.MIN_VALUE);
        System.out.printf("%d\n", Integer.MAX_VALUE + 1);
        System.out.printf("%d\n", 19 + 2 * 3);
        System.out.printf("%d\n", (19 + 2) * 3);
    }
}
